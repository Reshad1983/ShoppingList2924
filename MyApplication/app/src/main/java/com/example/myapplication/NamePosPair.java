package com.example.myapplication;

public class NamePosPair {
    private String name;
    private int pos;
    public NamePosPair(String name, int pos )
    {
        this.name = name;
        this.pos = pos;
    }

    public String getName()
    {
        return this.name;
    }

    public int getPos()
    {
        return this.pos;

    }
}
